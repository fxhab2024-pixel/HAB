"""Roadmap generator (MVP): turns analysis into 90-day actions."""

def generate_roadmap(analysis):
    score = analysis["score"]
    actions = []

    if score < 40:
        actions += [
            "بازتعریف ارزش پیشنهادی و بازار هدف (هفته ۱ تا ۲)",
            "شفافسازی مدل درآمدی و قیمتگذاری (هفته ۲ تا ۴)",
            "تعیین ۳ KPI حیاتی و ساخت داشبورد ساده (هفته ۳ تا ۶)",
            "طراحی برنامه بهبود نقدینگی و کنترل هزینه (هفته ۱ تا ۸)",
        ]
    elif score < 70:
        actions += [
            "انتخاب ۳ اولویت استراتژیک و توقف ۲ فعالیت کماثر (هفته ۱)",
            "بهینهسازی عملیات: استانداردسازی فرایندها و حذف کار دستی (هفته ۲ تا ۸)",
            "تعریف KPIها و جلسه هفتگی پایش (هفته ۱ تا ۱۲)",
            "آمادهسازی بسته داده مالی برای سرمایهپذیری (هفته ۴ تا ۱۲)",
        ]
    else:
        actions += [
            "تهیه Data Room اولیه (مالی، بازار، عملیات) برای ارزیابی (هفته ۱ تا ۴)",
            "برنامه رشد ۹۰ روزه با هدفگذاری درآمد/حاشیه سود (هفته ۱)",
            "طراحی سناریوهای تامین مالی و مصرف سرمایه (هفته ۲ تا ۶)",
            "پایلوت جذب سرمایه محدود/راهبردی پس از اجرای اصلاحات (هفته ۶ تا ۱۲)",
        ]

    return actions[:5]
